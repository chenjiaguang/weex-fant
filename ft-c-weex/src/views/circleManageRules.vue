    <text class="circle-manage-rules-para-text padding-block">第二条、圈子是基于共同兴趣的社区用户自主建立的群组产品，具有高度开放性、自治性的特点。圈子隶属范团社区，任何拥有范团社区帐号的用户，均可享受圈子服务。</text>
    <text class="circle-manage-rules-head">二、圈子管理细则</text>
    <text class="circle-manage-rules-para-text padding-block">第三条、创建圈子</text>
    <text class="circle-manage-rules-para-text">（一）指导原则</text>
